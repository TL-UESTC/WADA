def calculate_weighted_loss(preds, labels, weight, Epsilon):
    loss1 = torch.mean(weight * nn.BCEWithLogitsLoss(reduction='none')(preds, labels))
    loss2 = nn.BCEWithLogitsLoss()(preds, labels)
    return loss1 + loss2*Epsilon


